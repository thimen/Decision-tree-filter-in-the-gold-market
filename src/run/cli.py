from __future__ import annotations

import argparse

from src.run.configuration import parse_int_grid


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(description="Run the thesis-aligned MT5 experiment")
    parser.add_argument("--config", default=None, help="Path to JSON run configuration file")
    parser.add_argument("--write-config-template", default=None, help="Write a JSON template config to this path and exit")
    parser.add_argument("--results-dir", default=None, help="Output directory")
    parser.add_argument("--terminal-path", default=None, help="Path to MT5 terminal executable")
    parser.add_argument("--symbol", default=None, help="MT5 symbol")
    parser.add_argument("--timeframe", default=None, help="MT5 timeframe")
    parser.add_argument("--bars", type=int, default=None, help="Bars to fetch when UTC bounds are not used")
    parser.add_argument("--cost-bps", type=float, default=None)
    parser.add_argument("--initial-cash", type=float, default=None)
    parser.add_argument("--trade-size-pct", type=float, default=None)
    parser.add_argument("--take-profit-pct", type=float, default=None)
    parser.add_argument("--stop-loss-pct", type=float, default=None)
    parser.add_argument("--ema-fast", type=int, default=None)
    parser.add_argument("--ema-slow", type=int, default=None)
    parser.add_argument("--vote-k", type=int, default=None)
    parser.add_argument("--oscillator-vote-mode", choices=["trend", "mean_reversion"], default=None)
    parser.add_argument("--rsi-period", type=int, default=None)
    parser.add_argument("--rsi-buy", type=float, default=None)
    parser.add_argument("--rsi-sell", type=float, default=None)
    parser.add_argument("--macd-fast", type=int, default=None)
    parser.add_argument("--macd-slow", type=int, default=None)
    parser.add_argument("--macd-signal", type=int, default=None)
    parser.add_argument("--dmi-period", type=int, default=None)
    parser.add_argument("--mfi-period", type=int, default=None)
    parser.add_argument("--mfi-buy", type=float, default=None)
    parser.add_argument("--mfi-sell", type=float, default=None)
    parser.add_argument("--include-ema-vote", action=argparse.BooleanOptionalAction, default=None)
    parser.add_argument("--kst-roc1", type=int, default=None)
    parser.add_argument("--kst-roc2", type=int, default=None)
    parser.add_argument("--kst-roc3", type=int, default=None)
    parser.add_argument("--kst-roc4", type=int, default=None)
    parser.add_argument("--kst-sma1", type=int, default=None)
    parser.add_argument("--kst-sma2", type=int, default=None)
    parser.add_argument("--kst-sma3", type=int, default=None)
    parser.add_argument("--kst-sma4", type=int, default=None)
    parser.add_argument("--kst-signal", type=int, default=None)
    parser.add_argument("--enable-tree-model", action=argparse.BooleanOptionalAction, default=None)
    parser.add_argument("--label-horizon", type=int, default=None)
    parser.add_argument("--label-threshold", type=float, default=None)
    parser.add_argument("--tree-max-depth-grid", type=parse_int_grid, default=None)
    parser.add_argument("--tree-min-samples-leaf-grid", type=parse_int_grid, default=None)
    parser.add_argument("--tree-min-training-samples", type=int, default=None)
    parser.add_argument("--tree-random-state", type=int, default=None)
    parser.add_argument("--tree-holdout-ratio", type=float, default=None)
    parser.add_argument("--tree-time-series-splits", type=int, default=None)
    parser.add_argument("--confidence-bin-count", type=int, default=None)
    parser.add_argument("--confidence-binning-mode", choices=["quantile", "fixed_width"], default=None)
    parser.add_argument("--login", type=int, default=None)
    parser.add_argument("--password", default=None)
    parser.add_argument("--server", default=None)
    parser.add_argument("--timeout-ms", type=int, default=None)
    parser.add_argument("--portable", action=argparse.BooleanOptionalAction, default=None)
    parser.add_argument("--utc-from", default=None)
    parser.add_argument("--utc-to", default=None)
    parser.add_argument("--long-only", action=argparse.BooleanOptionalAction, default=None)
    return parser.parse_args()
